class Role < ApplicationRecord
  # this is a 2 way relationship. This is saying that a user has many roles and it belongs to many users.
  has_and_belongs_to_many :users, :join_table => :users_roles

  belongs_to :resource,
             :polymorphic => true,
             :optional => true

  validates :resource_type,
            :inclusion => { :in => Rolify.resource_types },
            :allow_nil => true

  scopify
end
